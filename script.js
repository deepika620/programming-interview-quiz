let selectedCategory=""
let selectedLevel=""
let questions=[]
let current=0
let score=0
let userAnswers=[]

let timer
let timeLeft=15


function chooseLevel(cat){

selectedCategory=cat

document.getElementById("categoryBox").classList.add("hide")
document.getElementById("levelBox").classList.remove("hide")

}


/* START QUIZ */

function startQuiz(level){

selectedLevel=level

let category=database[selectedCategory]

questions=[...category[selectedLevel]]

/* shuffle questions */

questions.sort(()=>Math.random()-0.5)

questions=questions.slice(0,10)

current=0
score=0
userAnswers=[]

document.getElementById("levelBox").classList.add("hide")
document.getElementById("quizBox").classList.remove("hide")

loadQuestion()

}


/* LOAD QUESTION */

function loadQuestion(){

clearInterval(timer)

if(current>=questions.length){

showResult()
return

}

let q=questions[current]

/* progress bar */

let progress=((current)/questions.length)*100
document.getElementById("progressBar").style.width=progress+"%"

document.getElementById("question").innerText=q.q

let html=""

q.o.forEach((opt,i)=>{

html+=`<button onclick="selectAnswer(${i})">${opt}</button>`

})

document.getElementById("options").innerHTML=html

startTimer()

}


/* TIMER */

function startTimer(){

timeLeft=15

document.getElementById("timer").innerText="Time: "+timeLeft

timer=setInterval(()=>{

timeLeft--

document.getElementById("timer").innerText="Time: "+timeLeft

if(timeLeft===0){

clearInterval(timer)

userAnswers.push(null)

current++

loadQuestion()

}

},1000)

}


/* SELECT ANSWER */

function selectAnswer(i){

clearInterval(timer)

let q=questions[current]

let correctSound=document.getElementById("correctSound")
let wrongSound=document.getElementById("wrongSound")

if(i===q.a){

score++

if(correctSound) correctSound.play()

}else{

if(wrongSound) wrongSound.play()

}

userAnswers.push(i)

current++

loadQuestion()

}


/* SHOW RESULT */

function showResult(){

document.getElementById("quizBox").classList.add("hide")
document.getElementById("resultBox").classList.remove("hide")

document.getElementById("score").innerText="Score: "+score+" / "+questions.length

showAnswers()
loadLeaderboard()

}


/* DARK MODE */

function toggleMode(){

document.body.classList.toggle("dark")

}


/* SHOW CORRECT ANSWERS */

function showAnswers(){

let html=""

questions.forEach((q,index)=>{

let correct=q.o[q.a]
let user = userAnswers[index] !== null ? q.o[userAnswers[index]] : null

html+=`
<p>
<b>Q${index+1}: ${q.q}</b><br>
Your Answer: ${user ? user : "Not Answered"}<br>
Correct Answer: <span style="color:green">${correct}</span>
</p>
<hr>
`

})

document.getElementById("answers").innerHTML=html

}


/* SAVE SCORE */

function saveScore(){

let name=document.getElementById("playerName").value

if(name==="") return

let leaderboard=JSON.parse(localStorage.getItem("quizLeaderboard")) || []

leaderboard.push({name:name,score:score})

leaderboard.sort((a,b)=>b.score-a.score)

leaderboard=leaderboard.slice(0,5)

localStorage.setItem("quizLeaderboard",JSON.stringify(leaderboard))

loadLeaderboard()

}


/* LOAD LEADERBOARD */

function loadLeaderboard(){

let leaderboard=JSON.parse(localStorage.getItem("quizLeaderboard")) || []

let html=""

leaderboard.forEach(player=>{

html+=`<li>${player.name} - ${player.score}</li>`

})

document.getElementById("leaderboard").innerHTML=html

}